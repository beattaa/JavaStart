package zajecia3.zadanie3;

public class MathClass {
    public static boolean isEven(int a) {
        return a % 2 == 0;
    }

    public static boolean isOdd(int a) {
        return a % 2 == 1;
    }

    public static double circleField(double r) {
        return Math.PI * r * r;
    }

    public static long power(int a) {
        return a * a;
    }
}
